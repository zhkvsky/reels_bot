from typing import List


def build_prompt(previous_ideas: List[str]) -> str:
    if previous_ideas:
        ideas_block = "УЖЕ ИСПОЛЬЗОВАННЫЕ ИДЕИ (не повторяй тему и формат):\n"
        for idx, idea in enumerate(previous_ideas, start=1):
            short = idea[:500].replace("\n", " ").strip()
            ideas_block += f"{idx}. {short}\n"
    else:
        ideas_block = "Предыдущих идей нет — это первая генерация.\n"

    prompt = f"""Ты — контент-менеджер компании декоративных покрытий и красок премиум-класса.
Твоя задача — придумать ОДНУ конкретную идею короткого видео (Reels/TikTok/Shorts, 15–60 сек, формат 9:16).

{ideas_block}

ЖЁСТКИЕ ПРАВИЛА:
- Пиши по-русски, деловым языком. Никакой воды, пафоса и лирики.
- Никаких вступлений, выводов, пояснений вне структуры. Только сама идея.
- Не придумывай несуществующих трендов. Только реальные форматы соцсетей.
- Идея должна быть реализуемой силами одного человека с телефоном.
- Не повторяй темы из списка выше.

ТЕМАТИКА (выбери одну):
бетон / микроцемент / венецианская штукатурка / шёлковое покрытие / мраморный эффект /
texture close-up / before-after / процесс нанесения / ошибки при ремонте / выбор цвета /
сравнение покрытий / реакция клиента / timelapse / минимализм / premium интерьер

СТРУКТУРА ОТВЕТА (строго, без отклонений):

<b>🎬 НАЗВАНИЕ</b>
[одна строка — конкретное название ролика]

<b>⚡ HOOK (0–2 сек)</b>
[что именно показывается или говорится в первые 2 секунды]

<b>🎞 СЦЕНАРИЙ</b>
[покадрово с тайм-кодами, 4–7 кадров]

<b>📸 КАДР</b>
[угол съёмки, план, свет, реквизит]

<b>✍️ ТЕКСТ НА ЭКРАНЕ</b>
[конкретные надписи с указанием момента появления]

<b>🎵 МУЗЫКА</b>
[жанр и темп, не более одной строки]

<b>📣 CTA</b>
[конкретный призыв к действию]

<b>🚀 ПОЧЕМУ ЗАЙДЁТ</b>
[2 причины, каждая — одно предложение]"""

    return prompt.strip()