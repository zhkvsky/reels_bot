"""
services/prompts.py — шаблоны промптов для генерации идей.
"""

from typing import List


def build_prompt(previous_ideas: List[str]) -> str:
    """
    Строит промпт для Gemini на основе предыдущих идей.

    Args:
        previous_ideas: Список предыдущих идей для исключения повторов.

    Returns:
        Готовый промпт в виде строки.
    """

    # Блок с предыдущими идеями (если есть)
    if previous_ideas:
        ideas_block = "📋 УЖЕ ИСПОЛЬЗОВАННЫЕ ИДЕИ (не повторяй их):\n"
        for idx, idea in enumerate(previous_ideas, start=1):
            # Берём только первые ~200 символов каждой идеи, чтобы не перегружать контекст
            short = idea[:200].replace("\n", " ").strip()
            ideas_block += f"{idx}. {short}...\n"
    else:
        ideas_block = "📋 Это первая генерация — предыдущих идей нет.\n"

    prompt = f"""
Ты — опытный контент-стратег и сценарист для соцсетей. Ты работаешь с компанией, \
которая производит декоративные покрытия и краски класса премиум: венецианская штукатурка, \
микроцемент, шёлковые и бархатные покрытия, фактурные краски, металлические и мраморные эффекты.

Твоя задача — придумать ОДНУ оригинальную идею короткого вирусного видео (Reels/TikTok/Shorts).

{ideas_block}

ТРЕБОВАНИЯ:
1. Идея должна быть абсолютно УНИКАЛЬНОЙ — не повторяй перечисленные выше концепции.
2. Выбери одну из тематик: luxury интерьер, бетон/микроцемент, шёлк, мрамор, \
texture close-up, before/after, wow-effect, ASMR, минимализм, дорогой интерьер, \
ошибки при ремонте, нанесение покрытия мастером, дизайнерские решения, premium aesthetics, \
цветовые тренды, сравнение покрытий, реакция клиента, timelapse нанесения, эмоциональная история.
3. Видео должно быть коротким (15–60 секунд) и адаптированным для вертикального формата 9:16.

📐 ФОРМАТ ОТВЕТА (строго соблюдай структуру и используй HTML-теги):

<b>🎬 [НАЗВАНИЕ РОЛИКА]</b>

<b>⚡ HOOK (первые 2 секунды)</b>
[Что зрителя зацепит с первого кадра — фраза, действие, звук]

<b>🎞️ СЦЕНАРИЙ ПОКАДРОВО</b>
[Подробное описание каждого кадра с тайм-кодами, например:
0:00–0:02 — ...
0:03–0:06 — ...
и т.д.]

<b>📸 ЧТО ПОКАЗЫВАТЬ В КАДРЕ</b>
[Конкретные визуальные детали: угол съёмки, крупность плана, объект, освещение, реквизит]

<b>✍️ ТЕКСТ НА ЭКРАНЕ</b>
[Все надписи, субтитры, заголовки поверх видео — с указанием момента появления]

<b>🎵 МУЗЫКА / АТМОСФЕРА</b>
[Описание музыкального настроения, темпа, жанра или конкретный трек/исполнитель]

<b>📣 CTA (призыв к действию)</b>
[Что зритель должен сделать: подписаться, написать в директ, сохранить, поставить реакцию]

<b>🚀 ПОЧЕМУ ЭТО ВИДЕО МОЖЕТ ЗАЛЕТЕТЬ</b>
[2–3 причины вирусного потенциала: триггер, эмоция, тренд, механика вовлечения]

---

Пиши только саму идею в указанном формате. Без вступлений, пояснений и комментариев вне структуры.
"""

    return prompt.strip()